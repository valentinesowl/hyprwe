"""Repository hygiene — the rules .gitignore states in prose, enforced.

.gitignore is a filter, not a guard: it only ever applies to files git does not
already track. Anything added with `git add -f`, or committed before a rule
existed, stays tracked forever and no amount of ignoring undoes it. These tests
are the guard, and they run against the index (`git ls-files`) rather than the
working tree, so they see what would actually be published.

The leak guards are the sharp end. Reference checkouts and scratch images live
beside this repo while hacking on it; a pasted screenshot in the root or a photo
wallpaper is a real leak of whatever else was on screen, not a tidiness issue.
Those tests exist to fail loudly in CI before a push, so keep them strict.
"""
import subprocess
from pathlib import Path

import pytest

from conftest import theme_ids, theme_paths

IMAGE_SUFFIXES = {".png", ".jpg", ".jpeg", ".webp", ".gif", ".bmp"}


def tracked_files(repo: Path) -> list[Path]:
    out = subprocess.run(
        ["git", "ls-files", "-z"], cwd=repo, capture_output=True, text=True, check=True
    ).stdout
    return [Path(p) for p in out.split("\0") if p]


@pytest.fixture(scope="session")
def tracked(repo) -> list[Path]:
    files = tracked_files(repo)
    assert files, "git ls-files returned nothing — is this a git checkout?"
    return files


# ── NDA guards ────────────────────────────────────────────────────────────
def test_no_images_are_tracked_in_the_repo_root(tracked):
    """Pasted screenshots land here, and they leak the private project's context
    (internal GitLab, domain usernames, corporate tooling). Assets belong in
    assets/ or themes/<name>/, deliberately."""
    stray = [p for p in tracked if len(p.parts) == 1 and p.suffix.lower() in IMAGE_SUFFIXES]
    assert stray == [], (
        f"image(s) tracked in the repo root: {[str(p) for p in stray]} — "
        "move a real asset into assets/, or delete a pasted screenshot"
    )


def test_only_our_own_directories_are_tracked(tracked):
    """An allowlist, not a blocklist.

    Reference checkouts of other projects get cloned beside this one while
    hacking on it, and `git add -A` does not care whose code it is. Naming the
    trees to reject would mean naming them in a public file AND missing the next
    one; listing what HWE actually consists of catches any stray tree, whatever
    it is called. Adding a real top-level directory here is a one-line change.
    """
    ours = {
        ".github", "assets", "bin", "config", "lib", "pkg",
        "provision", "scripts", "templates", "tests", "themes",
    }
    stray = sorted({p.parts[0] for p in tracked if len(p.parts) > 1} - ours)
    assert stray == [], (
        f"tracked top-level director{'y' if len(stray) == 1 else 'ies'} not part of HWE: {stray} — "
        "if this is genuinely ours, add it to the allowlist in this test"
    )


def test_no_photo_wallpaper_libraries_are_tracked(tracked):
    """themes/<name>/wallpapers/ holds personal, likely third-party-copyrighted
    photos. The generated themes/<name>/wallpaper.png IS ours and is tracked."""
    photos = [p for p in tracked if len(p.parts) > 2 and p.parts[0] == "themes"
              and p.parts[2] == "wallpapers"]
    assert photos == [], f"photo library tracked: {[str(p) for p in photos]}"


# ── generated files must not be tracked ───────────────────────────────────
def test_generated_config_files_are_not_tracked(repo, tracked):
    """Every path .gitignore lists under 'generated by hwe theme' is regenerated
    on each apply; tracking one guarantees a permanently dirty tree."""
    tracked_set = {str(p) for p in tracked}
    generated = [
        line.strip()
        for line in (repo / ".gitignore").read_text().splitlines()
        if line.strip().startswith("config/") and "*" not in line and not line.startswith("#")
    ]
    assert generated, "could not find the generated-config block in .gitignore"
    offenders = [g for g in generated if g in tracked_set]
    assert offenders == [], f"generated file(s) tracked: {offenders}"


def test_the_working_tree_has_no_ignored_but_tracked_files(repo):
    """The definitive check: git itself reporting a tracked file it would ignore."""
    out = subprocess.run(
        ["git", "ls-files", "-i", "-c", "--exclude-standard"],
        cwd=repo, capture_output=True, text=True, check=True,
    ).stdout.split()
    assert out == [], f"tracked despite being ignored: {out}"


def test_no_python_bytecode_is_tracked(tracked):
    assert [str(p) for p in tracked if p.suffix == ".pyc" or "__pycache__" in p.parts] == []


