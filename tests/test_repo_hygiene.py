@pytest.mark.parametrize("theme_path", theme_paths(), ids=theme_ids())
def test_every_theme_ships_its_generated_assets(theme_path):
    """A fresh clone must never have to generate anything: the wallpaper and the
    picker's preview card are committed."""
    for asset in ("wallpaper.png", "preview.png"):
        path = theme_path.parent / asset
        assert path.is_file(), f"{theme_path.parent.name}: missing {asset} (run `just walls` / `just previews`)"
        assert path.stat().st_size > 0, f"{theme_path.parent.name}: {asset} is empty"


@pytest.mark.parametrize("theme_path", theme_paths(), ids=theme_ids())
def test_every_theme_asset_is_a_real_png(theme_path):
    for asset in ("wallpaper.png", "preview.png"):
        blob = (theme_path.parent / asset).read_bytes()
        assert blob[:8] == b"\x89PNG\r\n\x1a\n", f"{theme_path.parent.name}/{asset} is not a PNG"


# ── the package lists ─────────────────────────────────────────────────────
@pytest.mark.parametrize("lst", ["core.lst", "dev.lst", "vm.lst", "aur.lst"])
def test_package_lists_hold_one_package_per_line(repo, lst):
    """guest-install.sh strips comments and takes the rest verbatim — a stray
    space would make pacman look for a package that cannot exist."""
    path = repo / "pkg" / lst
    assert path.is_file(), f"pkg/{lst} is missing — guest-install.sh reads it"
    for lineno, line in enumerate(path.read_text().splitlines(), 1):
        pkg = line.split("#")[0].strip()
        if not pkg:
            continue
        assert " " not in pkg, f"pkg/{lst}:{lineno}: '{pkg}' has a space in it"


def test_the_quality_toolchain_is_installed_by_hwe_install(repo):
    """`hwe install` installs core.lst + dev.lst. Every tool the pipeline runs
    must be in one of them, or a contributor cannot reproduce CI locally."""
    declared = set()
    for lst in ("core.lst", "dev.lst"):
        for line in (repo / "pkg" / lst).read_text().splitlines():
            pkg = line.split("#")[0].strip()
            if pkg:
                declared.add(pkg)
    required = {
        "shellcheck", "shfmt", "bats", "bats-assert", "bats-support",
        "ruff", "python-pytest", "actionlint", "yamllint",
        "python-jinja", "python-numpy", "imagemagick", "just",
    }
    missing = required - declared
    assert missing == set(), f"quality tools not installed by `hwe install`: {sorted(missing)}"


